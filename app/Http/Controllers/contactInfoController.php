    /**
     * Show the form for creating a new resource.
     *
     * @return \Illuminate\Http\Response
     */
    public function create()
    {
        $contact = Contact::all();
        $info = [];
        
        
        if(isset($contact[0])){
            foreach($contact[0]->getAttributes() as $contactIndex => $contactVal){
                $info[$contactIndex] = $contactVal;
            }
        }

        // dd($info);
        
        return view('admin.contact.create')
            ->with('info', $info);
    }

    /**
     * Store a newly created resource in storage.
     *
     * @param  \Illuminate\Http\Request  $request
     * @return \Illuminate\Http\Response
     */
    public function store(Request $request)
    {
        $contact_old = Contact::where('id', '>', 0);
        $contact_old->delete();

        Contact::create($request->input());
        
        return redirect(route('admin.home'))->with('message', 'contact info created successfully');
    }

